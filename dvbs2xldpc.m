function H = dvbs2xldpc(R, varargin)
%DVBS2xLDPC Low-density parity-check codes from the DVB-S.2x standard.
%   H = DVBS2xLDPC(R) returns the parity-check matrix of the LDPC code with
%   code rate R from the DVB-S.2x standard. H is a sparse logical matrix.
%
%   H = DVBS2xLDPC(R, outputFormat) is the same as the previous syntax,
%   except that outputFormat specifies the format of H. The value of
%   outputFormat can be 'sparse' or 'indices'. When outputFormat is
%   'sparse', H is a sparse logical matrix. When you set the outputFormat
%   to 'indices', H is a two-column double-precision matrix that defines
%   the row and column indices of the 1s in the parity-check matrix. In
%   other words, if P = dvbs2xldpc(R, 'sparse') and I = dvbs2xldpc(R,
%   'indices'), then P = sparse(I(:,1), I(:,2), 1).
%
%   Possible values for R are 1/4, 1/3, 2/5, 1/2, 3/5, 2/3, 3/4, 4/5, 5/6,
%   8/9, 9/10, 2/9, 22/30, 132/180, 11/20, 9/20, 13/45. The block length of the code is 64800.
%
%   % Example:
%   %   Get the parity-check matrix for the rate 3/5 LDPC code from the
%   %   DVB-S.2x standard.
%
%   H = dvbs2xldpc(3/5);
%   spy(H); % visualize the location of nonzero elements in H
%   hEnc = comm.LDPCEncoder(H);
%   hDec = comm.LDPCDecoder(H);
%
%   See also comm.LDPCDecoder, comm.LDPCEncoder, SPY. 

narginchk(1,2);

lenCodeWord = 64800;  % Length of codeword for DVB-S.2x
NB = 360;  % Node indices parameter for DVB-S.2x

numInfoBits = lenCodeWord * R;
numParityBits = lenCodeWord - numInfoBits;

[v.ct1, v.ct2, v.ct3, v.ct4, v.ct5, v.ct6] = getchecknodetable(R);
d = [];
r = [];

for i = 1:6
    var_name = strcat('ct',num2str(i));
    [rows, ~] = size(v.(var_name));
    if rows ~= 0
        table = nodeindices(v.(var_name), numParityBits, NB);
        [~, dcols] = size(d);
        d(dcols + 1:dcols + 2) = [size(table,2) size(table,1)];
        newRow = table(:);
        r = [r(1:end, :); newRow];
    end
end

[~, dcols] = size(d);
d(dcols + 1:dcols + 4) = [numParityBits-1 2 1 1];
r = [r(1:end, :); 0; reshape(ones(2,1)*(1:numParityBits-1),[],1)];

S = zeros(length(r),1);
numGroup = length(d)/2;
n = 0;
ncol = 1;
for i = 1:numGroup
    p = d(2*i-1)*d(2*i);
    S(n+1:n+p) = reshape(ones(d(2*i),1)*(ncol:ncol+d(2*i-1)-1),p,1);
    ncol = ncol + d(2*i-1);
    n = n + p;
end

% Parity-check matrix (sparse) for DVB-S.2x
outputFormat = 'sparse'; % Sparse matrix by default
if nargin == 2
    if ~strcmp(varargin{1}, 'sparse') && ~strcmp(varargin{1}, 'indices')
        error(message('comm:dvbs2ldpc:InvalidOutputFormat'));
    end
    outputFormat = varargin{1};
end

if strcmp(outputFormat, 'sparse')
    H = logical(sparse(double(r+1), S, 1));
else
    H = [double(r+1), double(S)];
end

%--------------------------------------------------------------------------
function ck = nodeindices(ct, M, NB)
% ct: check node table (single group)
% M: number of parity bits
% NB: block size
[N, D] = size(ct);
q = (M/NB);
b = (1:NB);
bq = (b-1).'*q;
ck = zeros(D, NB*N);
for r=1:N
    ck(:, NB*(r-1)+1:NB*r) = mod(addcr(bq, ct(r,:)), M)';
end

%--------------------------------------------------------------------------
function A = addcr(c, r)
M = length(c);
N = length(r);
A = zeros(M, N);
for m = 1:M
    A(m, :) = r + c(m);
end

